import random  # นำเข้าโมดูล random เพื่อสุ่มตัวเลข

# ฟังก์ชันหลักของเกม
def guess_the_number():
    print("ยินดีต้อนรับสู่เกมทายตัวเลข!")
    print("เดาตัวเลขระหว่าง 1 ถึง 10")
    
    secret_number = random.randint(1, 10)  # สุ่มเลขระหว่าง 1 ถึง 10
    attempts = 0  # ตัวนับจำนวนครั้งที่ผู้เล่นเดา
    
    while True:  # ใช้ลูปเพื่อให้เกมทำงานต่อไปจนกว่าจะเดาถูก
        guess = input("ป้อนตัวเลขที่คุณเดา: ")  # รับค่าตัวเลขจากผู้เล่น
        
        # ตรวจสอบว่าอินพุตเป็นตัวเลขหรือไม่
        if not guess.isdigit():
            print("โปรดป้อนเฉพาะตัวเลขเท่านั้น!")
            continue  # กลับไปถามใหม่
        
        guess = int(guess)  # แปลงค่าที่ป้อนไปเป็นตัวเลข
        attempts += 1  # เพิ่มจำนวนครั้งที่เดา
        
        # ตรวจสอบว่าผู้เล่นเดาถูกหรือไม่
        if guess == secret_number:
            print(f"ยินดีด้วย! คุณทายถูกใน {attempts} ครั้ง")
            break  # จบเกม
        elif guess < secret_number:
            print("น้อยเกินไป! ลองใหม่อีกครั้ง")
        else:
            print("มากเกินไป! ลองใหม่อีกครั้ง")

# เริ่มเกม
guess_the_number()
